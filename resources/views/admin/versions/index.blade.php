<script>
const { createApp } = Vue;

createApp({
    data() {
        return {
            showImportModal: false,
            showExportModal: false,
            selectedVersion: '',
        }
    },
    methods: {
        importMarkdown() {
            if (!this.selectedVersion || !this.$refs.fileInput.files[0]) {
                alert('Please select both a version and a file.');
                return;
            }

            const formData = new FormData();
            formData.append('markdown_file', this.$refs.fileInput.files[0]);

            fetch(`/admin/version-manager/whats-new/import-markdown/${this.selectedVersion}`, {
                method: 'POST',
                body: formData,
                headers: {
                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                }
            })
            .then(response => {
                if (response.ok) {
                    window.location.reload();
                } else {
                    alert('Import failed. Please try again.');
                }
            })
            .catch(error => {
                console.error('Error:', error);
                alert('Import failed. Please try again.');
            });
        },
        exportMarkdown() {
            if (!this.selectedVersion) {
                alert('Please select a version.');
                return;
            }

            window.location.href = `/admin/version-manager/whats-new/export-markdown/${this.selectedVersion}`;
            this.showExportModal = false;
        }
    }
}).mount('#app');
</script>
